// Switch Statement
const balon = prompt("Enter a color (red, blue, green):");

switch (balon) {
  case "red":
    console.log("You chose red!");
    break;
  case "blue":
    console.log("You chose blue!");
    break;
  case "green":
    console.log("You chose green!");
    break;
  default:
    console.log("Color not recognized.");
}
